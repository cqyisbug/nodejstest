/**
 * 判断条件测试
 */


if (0) {
    console.log('test 0')//不输出
}

if (1) {
    console.log('test 1')//输出
}

if ('0') {
    console.log('test "0"')//输出
}

if ('1') {
    console.log('test "1"')//输出
}

if ({}) {
    console.log('test {}')//输出
}

if ([]) {
    console.log('test []')//输出
}

if (undefined) {
    console.log('test undefined')//不输出
}

if (null) {
    console.log('test null')//不输出
}


